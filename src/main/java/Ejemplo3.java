public class Ejemplo3 {
    public static void main(String[] args) {
        int repite = 10;

        while (repite > 0) {
            System.out.println("Hola");
            repite--;
        }

        for (int i = 10; i > 0; i--) {
            System.out.println("Adios");
        }

        int d = 2;
        do {
            System.out.println("Hola");
            d--;
        } while (d > 0);

    }
}
